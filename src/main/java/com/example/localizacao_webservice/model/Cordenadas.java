package main.java.com.example.localizacao_webservice.models;

public class Coordenadas {
    private double lat;
    private double lon;

    // Getters e Setters
    public double getLat() {
        return lat;
    }

    public void setLat(double lat) {
        this.lat = lat;
    }

    public double getLon() {
        return lon;
    }

    public void setLon(double lon) {
        this.lon = lon;
    }
}